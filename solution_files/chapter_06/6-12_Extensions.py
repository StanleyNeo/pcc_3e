# 扩展方案1：添加新键值（增加用户和数字）
喜欢的数字 = {
    '小美': [6, 8],          # 6和8在中国文化中代表顺利和发财
    '小明': [8, 9, 3],       # 9代表长久，3在部分地区有特殊含义
    '小刚': [5, 2],          # 5在中国文化中代表"我"，2代表"爱"
    # 新增用户
    '小红': [1, 9, 9, 7],    # 生日数字组合
    '老张': [8, 8, 8],       # 商业人士喜欢8的重复组合
    '学生': [100, 99]        # 学生偏好满分或高分数字
}

for 姓名, 数字列表 in 喜欢的数字.items():
    print(f"\n{姓名}喜欢的数字有（共{len(数字列表)}个）：")
    for 序号, 数字 in enumerate(数字列表, 1):
        print(f"  {序号}. {数字}")

# 扩展方案2：改变程序上下文（改为"幸运数字"并添加原因）
幸运数字 = {
    '小美': {
        '数字': [6, 8],
        '原因': ['六六大顺', '发财']
    },
    '小明': {
        '数字': [8, 9, 3],
        '原因': ['发财', '长久', '三生有幸']
    },
    '小刚': {
        '数字': [5, 2, 0],
        '原因': ['我爱您', None, '圆满']
    }
}

for 姓名, 数据 in 幸运数字.items():
    print(f"\n{姓名}的幸运数字：")
    for 数字, 原因 in zip(数据['数字'], 数据['原因']):
        原因说明 = f" - 因为{原因}" if 原因 else ""
        print(f"  {数字}{原因说明}")

# 扩展方案3：改进输出格式（添加统计和可视化）
喜欢的数字 = {
    '小美': [6, 8],
    '小明': [8, 9, 3],
    '小刚': [5, 2],
    '小红': [1, 9, 9, 7],
    '老张': [8, 8, 8]
}

# 统计数字出现频率
from collections import Counter
所有数字 = [数字 for 数字列表 in 喜欢的数字.values() for 数字 in 数字列表]
数字统计 = Counter(所有数字)

print("=== 数字偏好统计 ===")
print(f"总参与人数：{len(喜欢的数字)}")
print(f"最受欢迎数字：{数字统计.most_common(1)[0][0]}")
print(f"数字出现总次数：{len(所有数字)}")

# 可视化输出
print("\n=== 详细数据 ===")
for 姓名, 数字列表 in 喜欢的数字.items():
    print(f"\n{姓名}：{'★' * len(数字列表)}")
    print(f"  数字：{'、'.join(map(str, 数字列表))}")
    print(f"  数量：{len(数字列表)}个")

