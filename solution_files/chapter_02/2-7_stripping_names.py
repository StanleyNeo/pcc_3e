name = "\tEric Matthes\n" # add a tab \t   # add a newline \n
namec= "\t埃里克·马蒂斯\n"
print("Unmodified:")
print("未修改:")

print(name)
print(namec)

print("\nUsing lstrip():") # Stripping Whitespace # strip whitespace from the left side
print(name.lstrip())
print(namec.lstrip())

print("\nUsing rstrip():") # strip the whitespace from the right side
print(name.rstrip())
print(namec.rstrip())

print("\nUsing strip():") # strip whitespace from both side
print(name.strip())
print(namec.lstrip())

