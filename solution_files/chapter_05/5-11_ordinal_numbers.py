# Ordinal Numbers 20250608 SKNEO 序数词



数字表 = list(range(1,21))

for 数字 in 数字表:
    if 数字 == 1:
        print("1st 数字")
    elif 数字 == 2:
        print("2nd 数字")
    elif 数字 == 3:
        print("3rd 数字")
    else:
        print(f"{数字}th 数字")

numbers = list(range(1, 21))

print("\n-------------------------")
for number in numbers:
    if number == 1:
        print("1st")
    elif number == 2:
        print("2nd")
    elif number == 3:
        print("3rd")
    else:
        print(f"{number}th")
